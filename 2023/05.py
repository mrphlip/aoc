def maprange(m, a, b):
	for dst, src, n in m:
		if src < b and a < src + n:
			ra = max(src, a)
			rb = min(src+n, b)
			yield (ra - src + dst, rb - src + dst)
			if a < src:
				yield from maprange(m, a, src)
			if b > src + n:
				yield from maprange(m, src+n, b)
			return
	yield (a, b)
	return
def flatten(r):
	new = []
	r.sort()
	for a, b in r:
		if new and a <= new[-1][1]:
			new[-1][1] = max(new[-1][1], b)
		else:
			new.append([a,b])
	return new
def mapranges(m, s):
	new = []
	for r in s:
		new.extend(maprange(m, *r))
	return flatten(new)
